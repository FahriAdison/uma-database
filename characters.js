// Get random uma dengan weighted rarity
export function getRandomUma() {
  const roll = Math.random() * 100;
  
  let rarity;
  if (roll < 2) {
    rarity = 'SS';
  } else if (roll < 12) {
    rarity = 'S';
  } else {
    rarity = 'A';
  }
  
  const candidates = umaDatabase.filter(u => u.rarity === rarity);
  return candidates[Math.floor(Math.random() * candidates.length)];
}

// Get all SS rarity umas
export function getSSRarityUmas() {
  return umaDatabase.filter(u => u.rarity === 'SS');
}

// Get uma by ID
export function getUmaById(id) {
  return umaDatabase.find(u => u.id === id);
}

